package coding_practice;

public class Multiplywithusingoperator {

	public static void main(String[] args) {
		int num1 = 5;
		int num2 = 10;
		int sum=0;
		 int k =1;
		while(k<=num2) {
			
			sum=sum+num1;  
			
			k++;
		}
		
		System.out.println("Multiplication of "+num1+" & "+num2+" is: "+sum);
		

	}

}
